// Function to calculate total score of Rock, Paper, Scissors played according to strategy guide
function RPSScore(plan) {
    let totalScore = 0;
    // for each loop on plan
    plan.forEach(function(move) {
        const oppMove = move[0][0];
        const respMove = move[0][1];
        let shapeScore = 0;
        let outcomeScore = 0;
        // if opponent is rock
        if (oppMove === 'A') {
            // if response is rock
            if (respMove === 'X') {
                shapeScore = 1;
                outcomeScore = 3;
            }
            // if response is paper
            else if (respMove === 'Y') {
                shapeScore = 2;
                outcomeScore = 6;
            }
            // if response is scissors
            else {
                shapeScore = 3;
            }
        }

        // else if opponent is paper
        else if (oppMove === 'B') {
            // if response is rock
            if (respMove === 'X') {
                shapeScore = 1;
            }
            // if response is paper
            else if (respMove === 'Y') {
                shapeScore = 2;
                outcomeScore = 3;
            }
            // if response is scissors
            else {
                shapeScore = 3;
                outcomeScore = 6;
            }
        }
        // else (scissors)
        else {
            // if response is rock
            if (respMove === 'X') {
                shapeScore = 1;
                outcomeScore = 6;
            }
            // if response is paper
            else if (respMove === 'Y') {
                shapeScore = 2;
            }
            // if response is scissors
            else {
                shapeScore = 3;
                outcomeScore = 3;
            }
        }
        totalScore = totalScore + shapeScore + outcomeScore;
    })
    return totalScore;
}
